def celsius_to_farenheit(c):
    return (c * 9/5) + 32

def farenheit_to_celsius(f):
    return (f - 32) * 5/9

def celsius_to_kelvin(c):
    return c + 273.15

def kelvin_to_celsius(k):
    return k - 273.15

def kelvin_to_farenheit(k):
    return (k - 273.15) * 9/5 + 32

def farenheit_to_kelvin(f):
    return (f - 32) * 5/9 + 273.15
